// switch case syntax:
// switch (key) {
//     case value:
        
//         break;

//     default:
//         break;
// }

const month = 3
switch (month) {
    case 0:
        console.log("jan");
        break;
    case 1:
        console.log("feb");
        break;
    case 2:
        console.log("march");
        break;
    case 3:
        console.log("april");
        break;

    default:
        console.log("Default case match");
        break;
}